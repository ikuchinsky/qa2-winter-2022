package hw1;

public class Fridge {
    private String name;
    private double capacity;
    private int quantityOfDoors;
    private int quantityOfCells;
    private String color;

    //-------------GETTERS/SETTERS-----------


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getCapacity() {
        return capacity;
    }

    public void setCapacity(double capacity) {
        this.capacity = capacity;
    }

    public int getQuantityOfDoors() {
        return quantityOfDoors;
    }

    public void setQuantityOfDoors(int quantityOfDoors) {
        this.quantityOfDoors = quantityOfDoors;
    }

    public int getQuantityOfCells() {
        return quantityOfCells;
    }

    public void setQuantityOfCells(int quantityOfCells) {
        this.quantityOfCells = quantityOfCells;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
