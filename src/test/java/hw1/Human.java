package hw1;

public class Human {
   private String name;
   private int quantityOfFingers;
   private int quantityOfEyes;
   private long age;
   private float weight;
   private float growht;

   //-------------GETTERS/SETTERS-----------


   public String getName() {
      return name;
   }

   public void setName(String name) {
      this.name = name;
   }

   public int getQuantityOfFingers() {
      return quantityOfFingers;
   }

   public void setQuantityOfFingers(int quantityOfFingers) {
      this.quantityOfFingers = quantityOfFingers;
   }

   public int getQuantityOfEyes() {
      return quantityOfEyes;
   }

   public void setQuantityOfEyes(int quantityOfEyes) {
      this.quantityOfEyes = quantityOfEyes;
   }

   public long getAge() {
      return age;
   }

   public void setAge(long age) {
      this.age = age;
   }

   public float getWeight() {
      return weight;
   }

   public void setWeight(float weight) {
      this.weight = weight;
   }

   public float getGrowht() {
      return growht;
   }

   public void setGrowht(float growht) {
      this.growht = growht;
   }
}
