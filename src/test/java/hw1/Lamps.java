package hw1;

public class Lamps {
    private String color;
    private int quantityOfLampsInRoom;
    private double power;
    private double illumination;
    private double brightness;
    private double colorfulTemperature;

    //-------------GETTERS/SETTERS-----------

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getQuantityOfLampsInRoom() {
        return quantityOfLampsInRoom;
    }

    public void setQuantityOfLampsInRoom(int quantityOfLampsInRoom) {
        this.quantityOfLampsInRoom = quantityOfLampsInRoom;
    }

    public double getPower() {
        return power;
    }

    public void setPower(double power) {
        this.power = power;
    }

    public double getIllumination() {
        return illumination;
    }

    public void setIllumination(double illumination) {
        this.illumination = illumination;
    }

    public double getBrightness() {
        return brightness;
    }

    public void setBrightness(double brightness) {
        this.brightness = brightness;
    }

    public double getColorfulTemperature() {
        return colorfulTemperature;
    }

    public void setColorfulTemperature(double colorfulTemperature) {
        this.colorfulTemperature = colorfulTemperature;
    }
}
