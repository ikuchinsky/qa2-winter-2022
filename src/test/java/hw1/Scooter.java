package hw1;

public class Scooter {
    private String manufacture;
    private String color;
    private double maxSpeed;
    private double energyCharge;
    private int quantityOfWheels;

    //-------------GETTERS/SETTERS-----------

    public String getManufacture() {
        return manufacture;
    }

    public void setManufacture(String manufacture) {
        this.manufacture = manufacture;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public double getMaxSpeed() {
        return maxSpeed;
    }

    public void setMaxSpeed(double maxSpeed) {
        this.maxSpeed = maxSpeed;
    }

    public double getEnergyCharge() {
        return energyCharge;
    }

    public void setEnergyCharge(double energyCharge) {
        this.energyCharge = energyCharge;
    }

    public int getQuantityOfWheels() {
        return quantityOfWheels;
    }

    public void setQuantityOfWheels(int quantityOfWheels) {
        this.quantityOfWheels = quantityOfWheels;
    }
}
